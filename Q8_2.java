public class Q8_2 {
    public static void main(String[] args) {
        for (int count = -8; count <= 8; count++) {
            printGraph(count * count);
        }
    }

    public static void printGraph(int x) {
        for (int i = 0; i < x; i++) {
            System.out.print("*");
        }
        System.out.println("");
    }
}
